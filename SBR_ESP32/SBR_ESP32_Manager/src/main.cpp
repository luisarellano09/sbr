void Init(){

    // Disable brownout detector
    WRITE_PERI_REG(RTC_CNTL_BROWN_OUT_REG, 0); 
    Log.traceln("Disable brownout detector");

    // Serial Port
    Serial.begin(115200);

    // Logging
    Log.begin(LOG_LEVEL_VERBOSE, &Serial);
    Log.setLevel(LOG_LEVEL_TRACE);

    // Manager Instance
    manager = new Manager();

}








